public class Griff {
    private String name;
    private int strong;

    public Griff(String name, int strong) {
        this.name = name;
        this.strong = strong;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setStrong(int strong) {
        this.strong = strong;
    }

    public String getName() {
        return name;
    }

    public int getStrong() {
        return strong;
    }

}
