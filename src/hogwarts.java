public class hogwarts {
    private int magic;
    private int trans;

    public hogwarts(int magic, int trans) {
        this.magic = magic;
        this.trans = trans;
    }

    public void setMagic(int magic) {
        this.magic = magic;
    }

    public void setTrans(int trans) {
        this.trans = trans;
    }

    public int getMagic() {
        return magic;
    }

    public int getTrans() {
        return trans;
    }

}

